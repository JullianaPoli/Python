# Operadores de comparação são utilizados para comparar dois valores.

print("\n")

# operador == (igualdade)
saldo = 450
saque = 200

print(saldo == saque)

print("\n")

# operador == (diferença)
saldo = 450
saque = 200

print(saldo != saque)

print("\n")

# Operador Maior que (>)

saldo = 450
saque = 200
print(saldo > saque)

print("\n")

# Maior ou igual (>=) 

print(saldo >= saque)

print("\n")

# Operador Menor que (<)
saldo = 450
saque = 200
print(saldo < saque)

print("\n")

# Menor ou igual (<=) 

print(saldo <= saque)

print("\n")


