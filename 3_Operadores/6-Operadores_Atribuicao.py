# Operadores de atribuição são utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

print("\n")

# Atribuição Simples

saldo = 500

print(saldo)

print("\n")

# Atribuição com Adição

saldo = 500
saldo += 200

print(saldo)

print("\n")

# Atribuição com Subtração

saldo = 500
saldo -= 100

print(saldo)

print("\n")

# Atribuição com Multiplicação

saldo = 500
saldo *= 2

print(saldo)

print("\n")

# Atribuição com divisão

saldo = 500
saldo /= 5

print(saldo)

print("\n")

# Atribuição com divisão inteiro.
saldo = 500
saldo //= 5

print(saldo)

print("\n")

# Atribuição com Módulo
saldo = 500
saldo %= 480

print(saldo)

print("\n")

# Atribuição com Exponenciação
saldo = 80
saldo **=2

print(saldo)

print("\n")


