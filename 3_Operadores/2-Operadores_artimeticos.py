# Adição 
print(1 + 1)

print("\n")

# Subtração 
print(10-12)

print("\n")

# Multiplicação 
print(4*3)

print("\n")

# Divisão 
print(12 / 3)

print("\n")

# Divisão inteira (com a barra dupla // o valor retorna inteiro).

print(12 // 2) 

print("\n")

# Módulo (operador de resto de divisão)
print(10 % 3)

print("\n")

# Exponenciação (que seria 2³)
print(2 ** 3)